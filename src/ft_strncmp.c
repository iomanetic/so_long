#include "../includes/so_long.h"

int	ft_strncmp(const char *s1, const char *s2)
{
	if (!*s1 && !*s2)
		return (0);
	while ((*s1 != '\0' || *s2 != '\0'))
	{
		if (*s1 != *s2)
			return (0);
		s1++;
		s2++;
	}
	return (1);
}
